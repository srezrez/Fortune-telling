package by.htp.flower.bean;

public class Stem {
	private int height;
	private int width;
	public Stem(int height, int width){
		this.height = height;
		this.width = width;
	}
	public int getHeight(){
		return height;
	}
	public int getWidth(){
		return width;
	}
	public void setHeight(int height){
		this.height = height;
	}
	public void setWidth(int width){
		this.width = width;
	}

}
